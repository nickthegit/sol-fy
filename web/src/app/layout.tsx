export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const footerData = getFooterData();
  const headerData = getHeaderData();

  // Wait for the promises to resolve
  const [footer, header] = await Promise.all([footerData, headerData]);

  return (
    <html lang="en" className={`${comfortaa.variable} font-sans`}>
      <body>
        <Header
          siteLogo={header?.siteLogo}
          siteTitle={header?.siteTitle}
          email={header?.email}
          instagramLink={header?.instagramLink}
        />
        {children}
        <Footer
          siteTitle={footer?.siteTitle}
          footerLinks={footer?.footerLinks}
        />
      </body>
    </html>
  );
}
